/**
 * Adds two numbers together.
 * @param {number} x The first number.
 * @param {number} y The second number.
 * @return {Number} The sum of the two numbers.
 */
export function sum(x, y) {
  // assert(typeof x === 'number');
  // assert(typeof y === 'number');
  return x + y;
}

